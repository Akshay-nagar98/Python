#What is List? How will you reverse a list? 
'''A list is a data structure in Python that is a mutable, or changeable, ordered sequence of elements. Each element or value that is inside of a list is called an item.
Just as strings are defined as characters between quotes,
lists are defined by having values between square brackets [ ] .
The . reverse() method in Python is a built-in method that reverses the list in place. Reversing the list in place means that the method modifies and changes the original list.
It doesn't create a new list which is a copy of the original but with the list items in reverse order.'''

